public class VariableApp{
    public static void main(String[] args)  {
        String color = "Green";
        Integer Year_Start = 2025;
        String middle_initial = "G";
        boolean Do_You_Have_Pets = true;
        String Quote = "You'll get there";
        System.out.println("The color is: " + color);
        System.out.println("The starting year is: " + Year_Start);
        System.out.println("The middle initial is: " + middle_initial);
        System.out.println("Do you have pets? " + Do_You_Have_Pets);
        System.out.println("The quote is: " + Quote);
        int days_in_week = 7;
        double coffee_price = 4.99;
        char favorite_letter = 'M';
        boolean is_raining = false;
        System.out.println("There are " + days_in_week + " days in a week.");
        System.out.println("The price of coffee is $" + coffee_price + ".");
        System.out.println("My favorite letter is " + favorite_letter + ".");
        System.out.println("Is it raining outside? " + is_raining + ".");
    }
}
